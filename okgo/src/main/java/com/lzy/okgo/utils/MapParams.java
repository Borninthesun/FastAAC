public class MapParams {
    /**
     * 获取API HTTP 请求内容
     *
     * @param (params-- 请求参数)
     * @return 处理完成的请求内容
     */
    public static String getRequestMapParams(Map<String, Object> params) {
        JSONObject jsonParameter = new JSONObject();
        try {
            if (params != null && params.size() > 0) {
                for (String key : params.keySet()) {
                    jsonParameter.put(key, params.get(key));
                }
            }
        } catch (JSONException e) {
            e.printStackTrace();
        }
        return jsonParameter.toString();
    }

}
